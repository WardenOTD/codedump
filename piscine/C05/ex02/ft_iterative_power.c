int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	a;

	i = 0;
	a = nb;
	if (power < 0)
		return (0);
	else if (nb == 0 && power == 0)
		return (1);
	else if (power == 0)
		return (1);
	else if (i < power)
	{
		while (++i < power)
			nb *= a;
		return (nb);
	}
	else
		return (0);
}

char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i - 1] < 47 || (str[i - 1] > 90 && str[i - 1] < 97)
				|| (str[i - 1] > 122)) && (str[i] >= 'a' && str[i] <= 'z'))
			str[i] -= 32;
		else if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}

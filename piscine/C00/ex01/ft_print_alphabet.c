#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	ascii;

	ascii = 'a';
	while (ascii <= 'z')
	{
		ft_putchar(ascii);
		ascii++;
	}
	ft_putchar('\n');
}

#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 1 && str[i] <= 31) || (str[i] >= 58 && str[i] <= 126)
			|| (str[i] >= 33 && str[i] <= 47))
			return (0);
		else if ((str[i] >= '0' && str[i] <= '9') || str[i] == ' ')
			i++;
	}
	return (i);
}
